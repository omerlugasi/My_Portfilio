class Flowers102FromJson(Dataset):
    def __init__(self, images_root: Path, labels_json: Path, image_size: int = 64):
        assert image_size >= 64, "HW rule: do not resize below 64x64."
        self.images_root = images_root

        with open(labels_json, "r") as f:
            mapping = json.load(f)

        samples = []

        for class_key, img_list in mapping.items():
              class_id = int(class_key)
              for img_rel in img_list:
                  samples.append((img_rel, class_id))

        self.samples = samples

        self.transform = transforms.Compose([
            transforms.Resize((image_size, image_size)),
            transforms.CenterCrop((image_size, image_size)),
            transforms.ToTensor(),
            transforms.Normalize((0.5,)*3, (0.5,)*3),
        ])

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx: int):
        img_rel, class_id = self.samples[idx]
        img_path = self.images_root / img_rel
        if not img_path.exists():
            img_path = self.images_root / Path(img_rel).name

        image = Image.open(img_path).convert("RGB")
        image = self.transform(image)
        return image, class_id, str(img_path)

