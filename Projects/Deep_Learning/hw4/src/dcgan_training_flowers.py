"""**DCGAN Models (Generator + Discriminator) + init**"""

def weights_init_dcgan(m):
    name = m.__class__.__name__
    if "Conv" in name or "Linear" in name:
        nn.init.normal_(m.weight.data, 0.0, 0.02)
        if getattr(m, "bias", None) is not None and m.bias is not None:
            nn.init.constant_(m.bias.data, 0.0)
    elif "BatchNorm" in name:
        nn.init.normal_(m.weight.data, 1.0, 0.02)
        nn.init.constant_(m.bias.data, 0.0)

class DCGANGenerator(nn.Module):
    def __init__(self, latent_dim: int = 128, base_channels: int = 64, image_channels: int = 3):
        super().__init__()
        self.net = nn.Sequential(
            nn.ConvTranspose2d(latent_dim, base_channels*8, 4, 1, 0, bias=False),  # 4x4
            nn.BatchNorm2d(base_channels*8),
            nn.ReLU(True),

            nn.ConvTranspose2d(base_channels*8, base_channels*4, 4, 2, 1, bias=False),  # 8x8
            nn.BatchNorm2d(base_channels*4),
            nn.ReLU(True),

            nn.ConvTranspose2d(base_channels*4, base_channels*2, 4, 2, 1, bias=False),  # 16x16
            nn.BatchNorm2d(base_channels*2),
            nn.ReLU(True),

            nn.ConvTranspose2d(base_channels*2, base_channels, 4, 2, 1, bias=False),    # 32x32
            nn.BatchNorm2d(base_channels),
            nn.ReLU(True),

            nn.ConvTranspose2d(base_channels, image_channels, 4, 2, 1, bias=False),     # 64x64
            nn.Tanh()  # output in [-1,1]
        )

    def forward(self, z):
        return self.net(z)

class DCGANDiscriminator(nn.Module):
    def __init__(self, base_channels: int = 64, image_channels: int = 3):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv2d(image_channels, base_channels, 4, 2, 1, bias=False),  # 32x32
            nn.LeakyReLU(0.2, inplace=True),

            nn.Conv2d(base_channels, base_channels*2, 4, 2, 1, bias=False),  # 16x16
            nn.BatchNorm2d(base_channels*2),
            nn.LeakyReLU(0.2, inplace=True),

            nn.Conv2d(base_channels*2, base_channels*4, 4, 2, 1, bias=False),  # 8x8
            nn.BatchNorm2d(base_channels*4),
            nn.LeakyReLU(0.2, inplace=True),

            nn.Conv2d(base_channels*4, base_channels*8, 4, 2, 1, bias=False),  # 4x4
            nn.BatchNorm2d(base_channels*8),
            nn.LeakyReLU(0.2, inplace=True),

            nn.Conv2d(base_channels*8, 1, 4, 1, 0, bias=False)  # (B,1,1,1)
        )

    def forward(self, x):
        logits = self.net(x)
        return logits.view(x.size(0))  # (B,)

