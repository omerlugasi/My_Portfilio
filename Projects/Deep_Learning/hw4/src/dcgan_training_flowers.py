def train_dcgan(generator, discriminator, dataloader, opt_g, opt_d, criterion,
                latent_dim=128, num_epochs=10, log_every=200):
    g_losses, d_losses = [], []
    step = 0

    generator.train()
    discriminator.train()

    for epoch in range(num_epochs):
        for real_images, _, _ in dataloader:
            real_images = real_images.to(device)
            batch_size = real_images.size(0)

            # -------------------------
            # 1) Train Discriminator
            # -------------------------
            opt_d.zero_grad(set_to_none=True)

            real_targets = torch.full((batch_size,), 0.9, device=device)
            fake_targets = torch.zeros(batch_size, device=device)

            real_logits = discriminator(real_images)
            loss_d_real = criterion(real_logits, real_targets)

            noise = torch.randn(batch_size, latent_dim, 1, 1, device=device)
            fake_images = generator(noise).detach()
            fake_logits = discriminator(fake_images)
            loss_d_fake = criterion(fake_logits, fake_targets)

            loss_d = loss_d_real + loss_d_fake
            loss_d.backward()
            opt_d.step()

            # -------------------------
            # 2) Train Generator
            # -------------------------
            opt_g.zero_grad(set_to_none=True)

            noise = torch.randn(batch_size, latent_dim, 1, 1, device=device)
            generated_images = generator(noise)
            gen_logits = discriminator(generated_images)

            loss_g = criterion(gen_logits, real_targets)  # non-saturating
            loss_g.backward()
            opt_g.step()

            d_losses.append(loss_d.item())
            g_losses.append(loss_g.item())

            if step % log_every == 0:
                print(f"epoch {epoch+1}/{num_epochs} | step {step} | loss_D={loss_d.item():.4f} | loss_G={loss_g.item():.4f}")
            step += 1

        show_generated(generator, fixed_noise, title=f"Generated after epoch {epoch+1}")

    return g_losses, d_losses

