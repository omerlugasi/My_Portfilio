def show_pair(i, j, title):
    img_i = (fake_pool[i] * 0.5 + 0.5).clamp(0,1)
    img_j = (fake_pool[j] * 0.5 + 0.5).clamp(0,1)

    zi = z_flat(z_pool[i])
    zj = z_flat(z_pool[j])

    l2 = torch.norm(zi - zj, p=2).item()
    cos = torch.nn.functional.cosine_similarity(zi.unsqueeze(0), zj.unsqueeze(0)).item()

    plt.figure(figsize=(7,3))
    plt.subplot(1,2,1); plt.imshow(img_i.permute(1,2,0)); plt.axis("off"); plt.title(f"idx {i}")
    plt.subplot(1,2,2); plt.imshow(img_j.permute(1,2,0)); plt.axis("off"); plt.title(f"idx {j}")
    plt.suptitle(f"{title} | L2(z)={l2:.2f} | cos(z)={cos:.2f}")
    plt.tight_layout()
    plt.show()

def plot_z_pairs(pairs, title):
    plt.figure(figsize=(12,4))
    for k, (i, j) in enumerate(pairs, 1):
        zi = z_flat(z_pool[i]).numpy()
        zj = z_flat(z_pool[j]).numpy()
        plt.plot(zi, label=f"pair{k}: idx{i} (solid)")
        plt.plot(zj, linestyle="--", label=f"pair{k}: idx{j} (dashed)")
    plt.title(title)
    plt.xlabel("latent dimension")
    plt.ylabel("z value")
    plt.legend(ncol=3, fontsize=9)
    plt.grid(True, alpha=0.2)
    plt.show()

